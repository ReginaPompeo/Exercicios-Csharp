namespace ContagemRegressivaFor;

public class Program
{
    public static void Main()
    {
        Console.WriteLine("===== Contagem regressiva com for =====");

        for (int i = 10; i <= 10 && i >= 0; i--)
        {
            Console.WriteLine(i);
        }
        Console.WriteLine("Fim da contagem regressiva!l7ç");
    }
}